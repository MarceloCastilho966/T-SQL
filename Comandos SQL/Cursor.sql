DECLARE @NOME VARCHAR (200)
DECLARE CURSOR1 CURSOR FOR SELECT TOP 4 [nome] FROM [TABELA DE CLIENTES] tdc 
OPEN CURSOR1 
FETCH NEXT FROM CURSOR1 INTO @NOME
WHILE @@FETCH_STATUS = 0
BEGIN 
	PRINT @NOME 
	FETCH NEXT FROM CURSOR1 INTO @NOME
END

