DECLARE @NOME VARCHAR (200)
DECLARE @ENDERECO VARCHAR (MAX)
DECLARE CURSOR3 CURSOR FOR 
SELECT NOME, ([ENDERECO 1] + ' - ' + BAIRRO + ' - '  + CIDADE + ' - ' + ESTADO + ' - ' + CEP) END_COMPLETO
FROM [TABELA DE CLIENTES] tdc  
OPEN CURSOR3 
FETCH NEXT FROM CURSOR3 INTO  @NOME, @ENDERECO 
WHILE @@FETCH_STATUS = 0
BEGIN 
	PRINT @NOME + ' Endereco: ' + @ENDERECO
	FETCH NEXT FROM CURSOR3 INTO @NOME, @ENDERECO
END



